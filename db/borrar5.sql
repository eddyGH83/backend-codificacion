Con respecto ala implementación del trigger:

El trigger se implementó el pasado lunes 09 de octubre al 11 de octubre de 2024 y durante este tiempo de prueba los compañeros de codificación 
reportaron lentitud en el proceso de codificación y supervisión. Por lo tanto, se decidió desactivar el trigger el 12 de octubre de 2024. 

Los resultados de los tiempos de ejecucion del trigger se encuentran en el archivo adjunto "tiempos_trigger.xlsx". En el cual se puede observar
 claramente que el trigger afecta el rendimiento del sistema de codificacion.

Se propone una solución para actualizar la base de datos origen con los codigos correspondientes, actualizacion por día, este proceso se coordinará con alvaro.






